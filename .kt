// Basic Math operations in Kotlin
fun main() {
    val x = 10
    val y = 20

    // Addition
    val sum = x + y
    println("Sum of $x and $y is $sum")

    // Subtraction
    val diff = y - x
    println("Difference between $y and $x is $diff")

    // Multiplication
    val product = x * y
    println("Product of $x and $y is $product")

    // Division
    val quotient = y / x
    println("Quotient of $y and $x is $quotient")

    // Modulus (remainder)
    val remainder = y % x
    println("Remainder of $y divided by $x is $remainder")
}
